# Coupling and Cohesion

# A unit is any reasonable self-contained component 
# ex: a function, a file, a class, a set of modules

# Coupling: The linking of two separate units (i.e. making them a couple)
# In general, we want units to be loosely coupled
# Tightly coupled units are considered bad practice. Can cause large project to be 
# # # difficult to maintain.


# Cohesion: refers to the number and diversity of tasks that a single unit is
### responsible for.
# High Cohesion: a unit is responsible for 1 single task
# Low cohesion: a unit is responsible for many things
